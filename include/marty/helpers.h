namespace marty
{

template < typename T >
::std::vector< T > make_vector( const T &t1 )
   {
    ::std::vector< T > v;
    v.push_back(t1);
    return v;
   }

template < typename T >
::std::vector< T > make_vector( const T &t1
                              , const T &t2
                              )
   {
    ::std::vector< T > v;
    v.push_back(t1);
    v.push_back(t2);
    return v;
   }

template < typename T >
::std::vector< T > make_vector( const T &t1
                              , const T &t2
                              , const T &t3
                              )
   {
    ::std::vector< T > v;
    v.push_back(t1);
    v.push_back(t2);
    v.push_back(t3);
    return v;
   }

template < typename T >
::std::vector< T > make_vector( const T &t1
                              , const T &t2
                              , const T &t3
                              , const T &t4
                              )
   {
    ::std::vector< T > v;
    v.push_back(t1);
    v.push_back(t2);
    v.push_back(t3);
    v.push_back(t4);
    return v;
   }

template < typename T >
::std::vector< T > make_vector( const T &t1
                              , const T &t2
                              , const T &t3
                              , const T &t4
                              , const T &t5
                              )
   {
    ::std::vector< T > v;
    v.push_back(t1);
    v.push_back(t2);
    v.push_back(t3);
    v.push_back(t4);
    v.push_back(t5);
    return v;
   }










}; // namespace marty



#endif /* MARTY_HELPERS_H */

